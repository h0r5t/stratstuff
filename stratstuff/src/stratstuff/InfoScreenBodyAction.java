package stratstuff;

public class InfoScreenBodyAction {

	private String displayName, inputName;

	public InfoScreenBodyAction(String displayName, String inputName) {
		this.displayName = displayName;
		this.inputName = inputName;
	}

	public String getDisplayName() {
		return displayName;
	}

	public String getInputName() {
		return inputName;
	}
}
